/// A single billboard instance with position, size, texture, and facing mode.
#[derive(Debug, Clone)]
pub struct Billboard {
    /// World-space position (centre of the billboard).
    pub position: Vec3,
    /// Half-width and half-height in world units.
    pub size: Vec2,
    /// Texture handle (index into the material system or atlas).
    pub texture_handle: u32,
    /// Facing mode.
    pub facing_mode: BillboardFacingMode,
    /// Tint colour (RGBA, premultiplied alpha).
    pub color: Vec4,
    /// Rotation angle in radians around the billboard's facing axis.
    pub rotation: f32,
    /// UV rectangle within the atlas: (u_min, v_min, u_max, v_max).
    pub uv_rect: Vec4,
    /// Velocity vector (only used when facing_mode is `Velocity`).
    pub velocity: Vec3,
    /// Sort depth (computed each frame during batching).
    pub sort_depth: f32,
    /// Custom data channel (shader-interpreted: e.g. animation frame, LOD fade).
    pub custom_data: Vec4,
}

impl Default for Billboard {
    fn default() -> Self {
        Self {
            position: Vec3::ZERO,
            size: Vec2::splat(1.0),
            texture_handle: 0,
            facing_mode: BillboardFacingMode::ScreenAligned,
            color: Vec4::ONE,
            rotation: 0.0,
            uv_rect: Vec4::new(0.0, 0.0, 1.0, 1.0),
            velocity: Vec3::ZERO,
            sort_depth: 0.0,
            custom_data: Vec4::ZERO,
        }
    }
}

impl Billboard {
    /// Create a simple screen-aligned billboard.
    pub fn new(position: Vec3, size: Vec2, texture_handle: u32) -> Self {
        Self {
            position,
            size,
            texture_handle,
            ..Default::default()
        }
    }

    /// Create a billboard with a specific facing mode.
    pub fn with_facing(mut self, mode: BillboardFacingMode) -> Self {
        self.facing_mode = mode;
        self
    }

    /// Set the tint colour.
    pub fn with_color(mut self, color: Vec4) -> Self {
        self.color = color;
        self
    }

    /// Set the rotation angle.
    pub fn with_rotation(mut self, radians: f32) -> Self {
        self.rotation = radians;
        self
    }

    /// Set the velocity (for `Velocity` facing mode).
    pub fn with_velocity(mut self, velocity: Vec3) -> Self {
        self.velocity = velocity;
        self
    }

    /// Set the UV rectangle.
    pub fn with_uv_rect(mut self, u_min: f32, v_min: f32, u_max: f32, v_max: f32) -> Self {
        self.uv_rect = Vec4::new(u_min, v_min, u_max, v_max);
        self
    }

    /// Compute the sort depth given a view matrix.
    pub fn compute_sort_depth(&mut self, view: &Mat4) {
        let view_pos = *view * self.position.extend(1.0);
        self.sort_depth = view_pos.z;
    }
}

