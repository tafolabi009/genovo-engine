// ---------------------------------------------------------------------------
// BillboardFacingMode
// ---------------------------------------------------------------------------

/// Determines how a billboard orients itself relative to the camera.
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum BillboardFacingMode {
    /// Billboard always faces the camera with no tilt; it is parallel to the
    /// screen plane. Good for particles, UI elements, and lens flares.
    ScreenAligned,
    /// Billboard rotates around the world Y axis only, staying upright. Good
    /// for trees, lampposts, and standing characters.
    WorldY,
    /// Billboard rotates around a custom axis. The axis is the rotation axis,
    /// and the billboard attempts to face the camera while constrained.
    AxisAligned(Vec3),
    /// Billboard is stretched along its velocity vector. The quad is elongated
    /// in the direction of motion, producing a streak effect. Good for sparks,
    /// rain drops, and tracer rounds.
    Velocity,
}

impl Default for BillboardFacingMode {
    fn default() -> Self {
        Self::ScreenAligned
    }
}

