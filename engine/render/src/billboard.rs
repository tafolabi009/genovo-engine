/// Generates the 4 vertices of a camera-facing quad for a single billboard.
pub struct BillboardQuadBuilder;

impl BillboardQuadBuilder {
    /// Generate the 4 corner positions and 6 indices for a billboard quad.
    ///
    /// The quad is centred at `position`, with half-extents `size`, oriented
    /// according to `facing_mode` relative to the camera.
    ///
    /// Returns `(positions[4], uvs[4])`.
    pub fn build_quad(
        billboard: &Billboard,
        camera_position: Vec3,
        camera_right: Vec3,
        camera_up: Vec3,
        camera_forward: Vec3,
    ) -> ([Vec3; 4], [Vec2; 4]) {
        let (right, up) = Self::compute_axes(
            billboard,
            camera_position,
            camera_right,
            camera_up,
            camera_forward,
        );

        let hw = billboard.size.x;
        let hh = billboard.size.y;

        // Apply rotation.
        let (right, up) = if billboard.rotation.abs() > 1e-6 {
            let cos_r = billboard.rotation.cos();
            let sin_r = billboard.rotation.sin();
            let rotated_right = right * cos_r + up * sin_r;
            let rotated_up = -right * sin_r + up * cos_r;
            (rotated_right, rotated_up)
        } else {
            (right, up)
        };

        let p = billboard.position;
        let positions = [
            p - right * hw - up * hh, // bottom-left
            p + right * hw - up * hh, // bottom-right
            p + right * hw + up * hh, // top-right
            p - right * hw + up * hh, // top-left
        ];

        let uv_rect = billboard.uv_rect;
        let uvs = [
            Vec2::new(uv_rect.x, uv_rect.w), // bottom-left
            Vec2::new(uv_rect.z, uv_rect.w), // bottom-right
            Vec2::new(uv_rect.z, uv_rect.y), // top-right
            Vec2::new(uv_rect.x, uv_rect.y), // top-left
        ];

        (positions, uvs)
    }

    /// Compute the right and up axes for a billboard given its facing mode.
    fn compute_axes(
        billboard: &Billboard,
        camera_position: Vec3,
        camera_right: Vec3,
        camera_up: Vec3,
        camera_forward: Vec3,
    ) -> (Vec3, Vec3) {
        match billboard.facing_mode {
            BillboardFacingMode::ScreenAligned => {
                // Use the camera's screen axes directly.
                (camera_right, camera_up)
            }

            BillboardFacingMode::WorldY => {
                // The up axis is always world Y. The right axis is the cross
                // product of world Y and the direction from billboard to camera.
                let to_camera = (camera_position - billboard.position).normalize_or_zero();
                let up = Vec3::Y;
                let right = up.cross(to_camera).normalize_or_zero();
                // Handle degenerate case where camera is directly above/below.
                if right.length_squared() < 1e-6 {
                    return (camera_right, up);
                }
                (right, up)
            }

            BillboardFacingMode::AxisAligned(axis) => {
                // Constrained rotation around the given axis.
                let axis_norm = axis.normalize_or_zero();
                let to_camera = (camera_position - billboard.position).normalize_or_zero();
                // Project to_camera onto the plane perpendicular to axis.
                let projected = (to_camera - axis_norm * axis_norm.dot(to_camera)).normalize_or_zero();
                let right = axis_norm.cross(projected).normalize_or_zero();
                if right.length_squared() < 1e-6 {
                    return (camera_right, axis_norm);
                }
                (right, axis_norm)
            }

            BillboardFacingMode::Velocity => {
                // Stretch along velocity direction.
                let vel_len = billboard.velocity.length();
                if vel_len < 1e-6 {
                    // No velocity: fall back to screen-aligned.
                    return (camera_right, camera_up);
                }
                let vel_dir = billboard.velocity / vel_len;
                // The up axis is along the velocity.
                let up = vel_dir;
                // The right axis is perpendicular to velocity and view direction.
                let to_camera = (camera_position - billboard.position).normalize_or_zero();
                let right = to_camera.cross(up).normalize_or_zero();
                if right.length_squared() < 1e-6 {
                    return (camera_right, up);
                }
                (right, up)
            }
        }
    }
}

