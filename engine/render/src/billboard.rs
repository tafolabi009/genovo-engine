// Billboard impostor rendering system for the Genovo engine.
//
// Replaces distant meshes with camera-facing textured quads. Supports multiple
// facing modes (screen-aligned, world-Y, axis-aligned, velocity-stretched),
// multi-angle impostor atlases with octahedral normal mapping, instanced
// batched rendering with back-to-front sorting, and automatic billboard cloud
// generation from complex meshes.

