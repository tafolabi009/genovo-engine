/// Represents a complex mesh as a set of billboard planes. Each plane is a
/// textured quad positioned and oriented to approximate one face cluster of
/// the original mesh.
///
/// Billboard clouds are generated by fitting planes to the mesh geometry using
/// a greedy algorithm that selects the plane covering the most surface area
/// at each step.
#[derive(Debug, Clone)]
pub struct BillboardCloud {
    /// The billboard planes making up the cloud.
    pub planes: Vec<BillboardCloudPlane>,
}

/// A single plane in a billboard cloud.
#[derive(Debug, Clone)]
pub struct BillboardCloudPlane {
    /// Centre position of the plane in object space.
    pub center: Vec3,
    /// Normal of the plane (the direction it faces).
    pub normal: Vec3,
    /// Right axis of the plane (tangent).
    pub right: Vec3,
    /// Up axis of the plane (bitangent).
    pub up: Vec3,
    /// Half-width of the plane.
    pub half_width: f32,
    /// Half-height of the plane.
    pub half_height: f32,
    /// UV offset within the atlas for this plane's texture.
    pub uv_offset: Vec2,
    /// UV scale within the atlas for this plane's texture.
    pub uv_scale: Vec2,
}

impl BillboardCloud {
    /// Generate a billboard cloud from a mesh.
    ///
    /// Fits `num_planes` billboard planes to approximate the mesh surface.
    pub fn generate(mesh: &Mesh, num_planes: usize) -> Self {
        let num_planes = num_planes.max(1);
        let tri_count = mesh.indices.len() / 3;

        if tri_count == 0 {
            return Self { planes: Vec::new() };
        }

        // Compute per-triangle data.
        let mut tri_normals: Vec<Vec3> = Vec::with_capacity(tri_count);
        let mut tri_centroids: Vec<Vec3> = Vec::with_capacity(tri_count);
        let mut tri_areas: Vec<f32> = Vec::with_capacity(tri_count);
        let mut assigned: Vec<bool> = vec![false; tri_count];

        for t in 0..tri_count {
            let base = t * 3;
            let p0 = Vec3::from_array(mesh.vertices[mesh.indices[base] as usize].position);
            let p1 = Vec3::from_array(mesh.vertices[mesh.indices[base + 1] as usize].position);
            let p2 = Vec3::from_array(mesh.vertices[mesh.indices[base + 2] as usize].position);

            let cross = (p1 - p0).cross(p2 - p0);
            let area = cross.length() * 0.5;
            let normal = cross.normalize_or_zero();
            let centroid = (p0 + p1 + p2) / 3.0;

            tri_normals.push(normal);
            tri_centroids.push(centroid);
            tri_areas.push(area);
        }

        let mut planes = Vec::with_capacity(num_planes);

        for _ in 0..num_planes {
            // Find the unassigned triangle with the largest area.
            let mut best_tri: Option<usize> = None;
            let mut best_area: f32 = -1.0;

            for t in 0..tri_count {
                if !assigned[t] && tri_areas[t] > best_area {
                    best_area = tri_areas[t];
                    best_tri = Some(t);
                }
            }

            let seed_tri = match best_tri {
                Some(t) => t,
                None => break, // All triangles assigned.
            };

            let plane_normal = tri_normals[seed_tri];
            let plane_center = tri_centroids[seed_tri];

            // Gather all unassigned triangles close to this plane.
            let normal_threshold = 0.7; // cos(~45 degrees)
            let mut cluster_tris: Vec<usize> = Vec::new();

            for t in 0..tri_count {
                if assigned[t] {
                    continue;
                }

                let normal_dot = tri_normals[t].dot(plane_normal);
                if normal_dot < normal_threshold {
                    continue;
                }

                // Distance from triangle centroid to plane.
                let dist = (tri_centroids[t] - plane_center).dot(plane_normal).abs();
                let mesh_extent = mesh.bounds.radius();
                if dist > mesh_extent * 0.3 {
                    continue;
                }

                cluster_tris.push(t);
            }

            // Mark all cluster triangles as assigned.
            for &t in &cluster_tris {
                assigned[t] = true;
            }

            if cluster_tris.is_empty() {
                assigned[seed_tri] = true;
                continue;
            }

            // Compute the plane's tangent frame.
            let (right, up) = compute_tangent_frame(plane_normal);

            // Project all cluster vertices onto the plane to compute extents.
            let mut min_right: f32 = f32::MAX;
            let mut max_right: f32 = f32::MIN;
            let mut min_up: f32 = f32::MAX;
            let mut max_up: f32 = f32::MIN;
            let mut centroid_sum = Vec3::ZERO;
            let mut point_count = 0;

            for &t in &cluster_tris {
                let base = t * 3;
                for k in 0..3 {
                    let p = Vec3::from_array(
                        mesh.vertices[mesh.indices[base + k] as usize].position,
                    );
                    let local = p - plane_center;
                    let r = local.dot(right);
                    let u = local.dot(up);
                    min_right = min_right.min(r);
                    max_right = max_right.max(r);
                    min_up = min_up.min(u);
                    max_up = max_up.max(u);
                    centroid_sum += p;
                    point_count += 1;
                }
            }

            let center = if point_count > 0 {
                centroid_sum / point_count as f32
            } else {
                plane_center
            };

            let half_width = ((max_right - min_right) * 0.5).max(0.01);
            let half_height = ((max_up - min_up) * 0.5).max(0.01);

            planes.push(BillboardCloudPlane {
                center,
                normal: plane_normal,
                right,
                up,
                half_width,
                half_height,
                uv_offset: Vec2::ZERO,
                uv_scale: Vec2::ONE,
            });
        }

        Self { planes }
    }

    /// Convert the billboard cloud to a renderable mesh (for preview or
    /// fallback rendering).
    pub fn to_mesh(&self) -> Mesh {
        let mut builder = MeshBuilder::new();

        for plane in &self.planes {
            let hw = plane.half_width;
            let hh = plane.half_height;
            let c = plane.center;
            let r = plane.right;
            let u = plane.up;
            let n = plane.normal;

            let p0 = c - r * hw - u * hh;
            let p1 = c + r * hw - u * hh;
            let p2 = c + r * hw + u * hh;
            let p3 = c - r * hw + u * hh;

            let uv0 = plane.uv_offset;
            let uv1 = plane.uv_offset + Vec2::new(plane.uv_scale.x, 0.0);
            let uv2 = plane.uv_offset + plane.uv_scale;
            let uv3 = plane.uv_offset + Vec2::new(0.0, plane.uv_scale.y);

            let v0 = builder.add_vertex(p0, n, uv0);
            let v1 = builder.add_vertex(p1, n, uv1);
            let v2 = builder.add_vertex(p2, n, uv2);
            let v3 = builder.add_vertex(p3, n, uv3);

            builder.add_quad(v0, v1, v2, v3);
        }

        builder.build_no_tangents()
    }

    /// Number of planes in the cloud.
    pub fn plane_count(&self) -> usize {
        self.planes.len()
    }

    /// Total triangle count (2 per plane).
    pub fn triangle_count(&self) -> usize {
        self.planes.len() * 2
    }
}

/// Compute a tangent frame (right, up) for a given normal vector.
fn compute_tangent_frame(normal: Vec3) -> (Vec3, Vec3) {
    let n = normal.normalize_or_zero();
    let reference = if n.y.abs() < 0.9 { Vec3::Y } else { Vec3::X };
    let right = n.cross(reference).normalize_or_zero();
    let up = right.cross(n).normalize_or_zero();
    (right, up)
}

