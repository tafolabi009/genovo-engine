/// WGSL code for soft-particle depth fade.
const SOFT_PARTICLE_WGSL: &str = r#"
// Soft particle depth fade.
// Reads the scene depth buffer and fades the particle near depth intersections.
//
// Usage:
//   let fade = soft_particle_fade(frag_depth, scene_depth, fade_distance);
//   color.a *= fade;

fn soft_particle_fade(frag_depth: f32, scene_depth: f32, fade_distance: f32) -> f32 {
    let depth_diff = scene_depth - frag_depth;
    return saturate(depth_diff / max(fade_distance, 0.0001));
}
"#;

