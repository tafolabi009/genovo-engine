    fn test_impostor_renderer_creation() {
        let imp = ImpostorRenderer::new(8, 4);
        assert_eq!(imp.total_angles(), 32);
        assert_eq!(imp.view_directions.len(), 32);

        // All view directions should be unit vectors.
        for dir in &imp.view_directions {
            assert!(
                (dir.length() - 1.0).abs() < 1e-4,
                "Non-unit view direction: {dir:?}"
            );
        }
    }

    #[test]
    fn test_impostor_tile_set_get() {
        let mut imp = ImpostorRenderer::new_horizontal(8);
        imp.set_tile(3, 42);
        assert_eq!(imp.get_tile(3), 42);
        assert_eq!(imp.get_tile(0), 0); // default
    }

    #[test]
    fn test_atlas_full() {
        let mut atlas = BillboardAtlas::new(2, 64, 0);
        // 2x2 = 4 tiles.
        for i in 0..4 {
            assert!(atlas.allocate_tile(i).is_some());
        }
        // 5th should fail.
        assert!(atlas.allocate_tile(99).is_none());
        assert_eq!(atlas.free_tile_count(), 0);
    }

    #[test]
    fn test_billboard_batch_bytes() {
        let mut batch = BillboardBatch::new();
        batch.submit(Billboard::new(Vec3::ZERO, Vec2::ONE, 0));

        let view = Mat4::IDENTITY;
        batch.build(Vec3::ZERO, Vec3::X, Vec3::Y, -Vec3::Z, &view);

        let vb = batch.vertex_bytes();
        assert_eq!(vb.len(), 4 * BillboardVertex::SIZE);

        let ib = batch.index_bytes();
        assert_eq!(ib.len(), 6 * std::mem::size_of::<u32>());
    }
