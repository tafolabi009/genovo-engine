/// Manages multi-angle impostor rendering. An impostor captures an object from
/// N viewpoints (arranged hemispherically) and stores each view as a tile in
/// the `BillboardAtlas`. At runtime, the best-matching angle is selected based
/// on the camera direction relative to the object.
#[derive(Debug, Clone)]
pub struct ImpostorRenderer {
    /// Number of angle subdivisions around the hemisphere (azimuth).
    pub num_azimuth: u32,
    /// Number of angle subdivisions from top to bottom (elevation).
    pub num_elevation: u32,
    /// Precomputed view directions for each impostor angle.
    pub view_directions: Vec<Vec3>,
    /// Atlas tile indices for each angle. Length = num_azimuth * num_elevation.
    pub angle_tiles: Vec<u32>,
    /// Whether to use octahedral mapping for normals and depth.
    pub use_octahedral: bool,
}

impl ImpostorRenderer {
    /// Create an impostor renderer with the given angular resolution.
    pub fn new(num_azimuth: u32, num_elevation: u32) -> Self {
        let total_angles = num_azimuth * num_elevation;
        let mut view_directions = Vec::with_capacity(total_angles as usize);

        for elev in 0..num_elevation {
            let theta = std::f32::consts::FRAC_PI_2 * (1.0 - elev as f32 / num_elevation.max(1) as f32);
            let cos_theta = theta.cos();
            let sin_theta = theta.sin();

            for az in 0..num_azimuth {
                let phi = 2.0 * std::f32::consts::PI * az as f32 / num_azimuth as f32;
                let dir = Vec3::new(
                    cos_theta * phi.cos(),
                    sin_theta,
                    cos_theta * phi.sin(),
                );
                view_directions.push(dir.normalize_or_zero());
            }
        }

        Self {
            num_azimuth,
            num_elevation,
            view_directions,
            angle_tiles: vec![0; total_angles as usize],
            use_octahedral: true,
        }
    }

    /// Create an 8-angle impostor (single elevation ring at horizon level).
    pub fn new_horizontal(num_angles: u32) -> Self {
        Self::new(num_angles, 1)
    }

    /// Select the best impostor angle for the given camera-to-object direction.
    ///
    /// Returns the angle index.
    pub fn select_angle(&self, camera_to_object: Vec3) -> usize {
        let dir = camera_to_object.normalize_or_zero();
        let mut best_idx = 0;
        let mut best_dot: f32 = -2.0;

        for (i, view_dir) in self.view_directions.iter().enumerate() {
            let dot = dir.dot(*view_dir);
            if dot > best_dot {
                best_dot = dot;
                best_idx = i;
            }
        }

        best_idx
    }

    /// Select the best impostor angle and also the second-best for blending.
    ///
    /// Returns `(best_idx, second_idx, blend_factor)`.
    pub fn select_angle_blended(&self, camera_to_object: Vec3) -> (usize, usize, f32) {
        let dir = camera_to_object.normalize_or_zero();
        let mut best_idx = 0;
        let mut best_dot: f32 = -2.0;
        let mut second_idx = 0;
        let mut second_dot: f32 = -2.0;

        for (i, view_dir) in self.view_directions.iter().enumerate() {
            let dot = dir.dot(*view_dir);
            if dot > best_dot {
                second_dot = best_dot;
                second_idx = best_idx;
                best_dot = dot;
                best_idx = i;
            } else if dot > second_dot {
                second_dot = dot;
                second_idx = i;
            }
        }

        // Blend factor: 0.0 = fully best, 1.0 = fully second.
        let range = (best_dot - second_dot).max(1e-6);
        let factor = 1.0 - (best_dot - second_dot) / range.max(0.01);

        (best_idx, second_idx, factor.clamp(0.0, 1.0))
    }

    /// Get the atlas tile index for an impostor angle.
    pub fn get_tile(&self, angle_index: usize) -> u32 {
        self.angle_tiles.get(angle_index).copied().unwrap_or(0)
    }

    /// Set the atlas tile index for an impostor angle.
    pub fn set_tile(&mut self, angle_index: usize, tile_index: u32) {
        if angle_index < self.angle_tiles.len() {
            self.angle_tiles[angle_index] = tile_index;
        }
    }

    /// Total number of angles.
    pub fn total_angles(&self) -> u32 {
        self.num_azimuth * self.num_elevation
    }
}

