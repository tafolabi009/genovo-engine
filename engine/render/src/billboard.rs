/// Complete WGSL vertex/fragment shader for billboard rendering.
pub const BILLBOARD_WGSL: &str = r#"
// Billboard rendering shader.
// Vertex shader expands per-instance billboard data into screen-aligned quads.
// Fragment shader applies texture, colour tint, and optional soft-particle fade.

struct BillboardInstance {
    @location(0) position: vec3<f32>,
    @location(1) size: vec2<f32>,
    @location(2) color: vec4<f32>,
    @location(3) uv_rect: vec4<f32>,
    @location(4) rotation: f32,
}

struct CameraUniforms {
    view: mat4x4<f32>,
    projection: mat4x4<f32>,
    view_projection: mat4x4<f32>,
    camera_position: vec3<f32>,
    camera_right: vec3<f32>,
    camera_up: vec3<f32>,
    viewport_size: vec2<f32>,
}

@group(0) @binding(0) var<uniform> camera: CameraUniforms;
@group(1) @binding(0) var billboard_texture: texture_2d<f32>;
@group(1) @binding(1) var billboard_sampler: sampler;

struct VertexOutput {
    @builtin(position) clip_position: vec4<f32>,
    @location(0) uv: vec2<f32>,
    @location(1) color: vec4<f32>,
}

// Quad corner offsets: BL, BR, TR, TL.
const QUAD_OFFSETS: array<vec2<f32>, 4> = array<vec2<f32>, 4>(
    vec2<f32>(-1.0, -1.0),
    vec2<f32>( 1.0, -1.0),
    vec2<f32>( 1.0,  1.0),
    vec2<f32>(-1.0,  1.0)
);

const QUAD_UVS: array<vec2<f32>, 4> = array<vec2<f32>, 4>(
    vec2<f32>(0.0, 1.0),
    vec2<f32>(1.0, 1.0),
    vec2<f32>(1.0, 0.0),
    vec2<f32>(0.0, 0.0)
);

@vertex
fn vs_main(
    instance: BillboardInstance,
    @builtin(vertex_index) vertex_index: u32,
) -> VertexOutput {
    var output: VertexOutput;

    let corner = QUAD_OFFSETS[vertex_index % 4u];
    let base_uv = QUAD_UVS[vertex_index % 4u];

    // Apply rotation.
    let cos_r = cos(instance.rotation);
    let sin_r = sin(instance.rotation);
    let rotated = vec2<f32>(
        corner.x * cos_r - corner.y * sin_r,
        corner.x * sin_r + corner.y * cos_r
    );

    // Expand to world-space offset.
    let right = camera.camera_right * rotated.x * instance.size.x;
    let up = camera.camera_up * rotated.y * instance.size.y;
    let world_pos = instance.position + right + up;

    output.clip_position = camera.view_projection * vec4<f32>(world_pos, 1.0);

    // Map base UV to atlas UV rect.
    output.uv = mix(
        instance.uv_rect.xy,
        instance.uv_rect.zw,
        base_uv
    );

    output.color = instance.color;

    return output;
}

@fragment
fn fs_main(input: VertexOutput) -> @location(0) vec4<f32> {
    let tex_color = textureSample(billboard_texture, billboard_sampler, input.uv);
    let final_color = tex_color * input.color;

    // Alpha test: discard near-transparent fragments.
    if (final_color.a < 0.01) {
        discard;
    }

    return final_color;
}
"#;

