/// Octahedral impostor mapping utilities for encoding normals and depth in a
/// 2D atlas tile, enabling parallax-correct rendering of impostors.
pub struct OctahedralMapping;

impl OctahedralMapping {
    /// Encode a unit direction vector to octahedral UV coordinates in [0, 1]^2.
    ///
    /// Maps a point on the unit sphere to a point on the unit square using an
    /// octahedron projection.
    pub fn encode(direction: Vec3) -> Vec2 {
        let n = direction.normalize_or_zero();
        let abs_sum = n.x.abs() + n.y.abs() + n.z.abs();
        let oct = Vec2::new(n.x, n.z) / abs_sum.max(1e-8);

        let oct = if n.y < 0.0 {
            Vec2::new(
                (1.0 - oct.y.abs()) * oct.x.signum(),
                (1.0 - oct.x.abs()) * oct.y.signum(),
            )
        } else {
            oct
        };

        // Map from [-1, 1] to [0, 1].
        oct * 0.5 + Vec2::splat(0.5)
    }

    /// Decode octahedral UV coordinates in [0, 1]^2 back to a unit direction.
    pub fn decode(uv: Vec2) -> Vec3 {
        // Map from [0, 1] to [-1, 1].
        let oct = uv * 2.0 - Vec2::ONE;

        let n = Vec3::new(oct.x, 1.0 - oct.x.abs() - oct.y.abs(), oct.y);

        let n = if n.y < 0.0 {
            Vec3::new(
                (1.0 - n.z.abs()) * n.x.signum(),
                n.y,
                (1.0 - n.x.abs()) * n.z.signum(),
            )
        } else {
            n
        };

        n.normalize_or_zero()
    }

    /// Generate the WGSL helper functions for octahedral encoding/decoding.
    pub fn wgsl_snippet() -> &'static str {
        OCTAHEDRAL_WGSL
    }
}

/// WGSL octahedral encoding/decoding functions.
const OCTAHEDRAL_WGSL: &str = r#"
fn octahedral_encode(n: vec3<f32>) -> vec2<f32> {
    let abs_sum = abs(n.x) + abs(n.y) + abs(n.z);
    var oct = vec2<f32>(n.x, n.z) / max(abs_sum, 0.00001);
    if (n.y < 0.0) {
        oct = vec2<f32>(
            (1.0 - abs(oct.y)) * sign(oct.x),
            (1.0 - abs(oct.x)) * sign(oct.y)
        );
    }
    return oct * 0.5 + 0.5;
}

fn octahedral_decode(uv: vec2<f32>) -> vec3<f32> {
    let oct = uv * 2.0 - 1.0;
    var n = vec3<f32>(oct.x, 1.0 - abs(oct.x) - abs(oct.y), oct.y);
    if (n.y < 0.0) {
        n = vec3<f32>(
            (1.0 - abs(n.z)) * sign(n.x),
            n.y,
            (1.0 - abs(n.x)) * sign(n.z)
        );
    }
    return normalize(n);
}
"#;

