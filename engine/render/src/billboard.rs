/// Per-vertex data for a billboard quad. Packed for GPU upload.
#[repr(C)]
#[derive(Debug, Clone, Copy)]
pub struct BillboardVertex {
    /// World-space position of this vertex.
    pub position: [f32; 3],
    /// Texture coordinates.
    pub uv: [f32; 2],
    /// RGBA colour (premultiplied alpha).
    pub color: [f32; 4],
    /// Billboard size (width, height) for instanced rendering.
    pub size: [f32; 2],
    /// Rotation angle in radians.
    pub rotation: f32,
}

impl BillboardVertex {
    /// Byte size of one vertex.
    pub const SIZE: usize = std::mem::size_of::<Self>();

    /// Create a new billboard vertex.
    pub fn new(position: Vec3, uv: Vec2, color: Vec4, size: Vec2, rotation: f32) -> Self {
        Self {
            position: position.to_array(),
            uv: uv.to_array(),
            color: color.to_array(),
            size: size.to_array(),
            rotation,
        }
    }
}

