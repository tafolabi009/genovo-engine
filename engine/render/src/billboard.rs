/// Packs multiple billboard textures into a single texture atlas.
///
/// Each atlas tile corresponds to one billboard type or one angle of a multi-
/// angle impostor. The atlas is a square texture subdivided into a regular grid.
#[derive(Debug, Clone)]
pub struct BillboardAtlas {
    /// Number of tiles along each axis.
    pub tiles_per_axis: u32,
    /// Total atlas width in pixels.
    pub atlas_width: u32,
    /// Total atlas height in pixels.
    pub atlas_height: u32,
    /// Tile width in pixels.
    pub tile_width: u32,
    /// Tile height in pixels.
    pub tile_height: u32,
    /// Number of allocated tiles.
    pub allocated_tiles: u32,
    /// Map from billboard type ID to tile index.
    pub tile_map: HashMap<u32, u32>,
    /// Padding between tiles in pixels.
    pub padding: u32,
}

impl BillboardAtlas {
    /// Create a new atlas with the given tile configuration.
    pub fn new(tiles_per_axis: u32, tile_size: u32, padding: u32) -> Self {
        let total = tile_size * tiles_per_axis + padding * (tiles_per_axis - 1);
        Self {
            tiles_per_axis,
            atlas_width: total,
            atlas_height: total,
            tile_width: tile_size,
            tile_height: tile_size,
            allocated_tiles: 0,
            tile_map: HashMap::new(),
            padding,
        }
    }

    /// Allocate a tile for a billboard type. Returns the tile index, or None if
    /// the atlas is full.
    pub fn allocate_tile(&mut self, type_id: u32) -> Option<u32> {
        if let Some(&idx) = self.tile_map.get(&type_id) {
            return Some(idx);
        }

        let max_tiles = self.tiles_per_axis * self.tiles_per_axis;
        if self.allocated_tiles >= max_tiles {
            return None;
        }

        let idx = self.allocated_tiles;
        self.allocated_tiles += 1;
        self.tile_map.insert(type_id, idx);
        Some(idx)
    }

    /// Get the UV rectangle for a tile index.
    ///
    /// Returns `(u_min, v_min, u_max, v_max)` in normalised [0, 1] coordinates.
    pub fn tile_uv_rect(&self, tile_index: u32) -> Vec4 {
        let col = tile_index % self.tiles_per_axis;
        let row = tile_index / self.tiles_per_axis;

        let tile_with_padding = self.tile_width + self.padding;
        let u_min = (col * tile_with_padding) as f32 / self.atlas_width as f32;
        let v_min = (row * tile_with_padding) as f32 / self.atlas_height as f32;
        let u_max = u_min + self.tile_width as f32 / self.atlas_width as f32;
        let v_max = v_min + self.tile_height as f32 / self.atlas_height as f32;

        Vec4::new(u_min, v_min, u_max, v_max)
    }

    /// Get the pixel rectangle for a tile index.
    pub fn tile_pixel_rect(&self, tile_index: u32) -> (u32, u32, u32, u32) {
        let col = tile_index % self.tiles_per_axis;
        let row = tile_index / self.tiles_per_axis;

        let tile_with_padding = self.tile_width + self.padding;
        let x = col * tile_with_padding;
        let y = row * tile_with_padding;

        (x, y, self.tile_width, self.tile_height)
    }

    /// Number of remaining free tiles.
    pub fn free_tile_count(&self) -> u32 {
        self.tiles_per_axis * self.tiles_per_axis - self.allocated_tiles
    }

    /// Total number of tiles.
    pub fn total_tile_count(&self) -> u32 {
        self.tiles_per_axis * self.tiles_per_axis
    }
}

