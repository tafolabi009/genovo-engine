/// Identified bottleneck in the render pipeline.
#[derive(Debug, Clone)]
pub struct Bottleneck {
    /// Name of the bottleneck pass or scope.
    pub name: String,
    /// Duration in microseconds.
    pub duration_us: f64,
    /// Percentage of total frame time.
    pub percentage: f64,
    /// Type of bottleneck.
    pub bottleneck_type: BottleneckType,
    /// Suggested mitigation.
    pub suggestion: String,
}

/// Type of rendering bottleneck.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum BottleneckType {
    /// GPU vertex processing bound.
    VertexBound,
    /// GPU fragment processing bound.
    FragmentBound,
    /// GPU compute bound.
    ComputeBound,
    /// GPU bandwidth bound.
    BandwidthBound,
    /// CPU command recording bound.
    CpuBound,
    /// Excessive overdraw.
    Overdraw,
    /// Too many draw calls.
    DrawCallBound,
    /// General long-running pass.
    LongPass,
}

/// Detect bottlenecks in a frame's pass profiles.
pub fn detect_bottlenecks(passes: &[PassProfile], total_frame_us: f64) -> Vec<Bottleneck> {
    let mut bottlenecks = Vec::new();

    for pass in passes {
        if pass.gpu_time_us < BOTTLENECK_THRESHOLD_US {
            continue;
        }

        let percentage = if total_frame_us > 0.0 {
            pass.gpu_time_us / total_frame_us * 100.0
        } else {
            0.0
        };

        // Check for excessive draw calls.
        if pass.draw_calls > 1000 {
            bottlenecks.push(Bottleneck {
                name: pass.name.clone(),
                duration_us: pass.gpu_time_us,
                percentage,
                bottleneck_type: BottleneckType::DrawCallBound,
                suggestion: format!(
                    "Reduce draw calls in '{}' (currently {}). Consider instancing or batching.",
                    pass.name, pass.draw_calls
                ),
            });
        }

        // Check for overdraw.
        let viewport_pixels = pass.resolution.0 as u64 * pass.resolution.1 as u64;
        let overdraw = pass.pipeline_stats.estimated_overdraw(viewport_pixels);
        if overdraw > 3.0 {
            bottlenecks.push(Bottleneck {
                name: pass.name.clone(),
                duration_us: pass.gpu_time_us,
                percentage,
                bottleneck_type: BottleneckType::Overdraw,
                suggestion: format!(
                    "High overdraw ({:.1}x) in '{}'. Consider better sorting or occlusion culling.",
                    overdraw, pass.name
                ),
            });
        }

        // Check for fragment-bound passes.
        if pass.pipeline_stats.fragment_shader_invocations > 0
            && pass.pipeline_stats.vertex_fragment_ratio() < 0.001
            && percentage > 15.0
        {
            bottlenecks.push(Bottleneck {
                name: pass.name.clone(),
                duration_us: pass.gpu_time_us,
                percentage,
                bottleneck_type: BottleneckType::FragmentBound,
                suggestion: format!(
                    "Pass '{}' appears fragment-bound ({:.1}% of frame). Consider reducing shader complexity or resolution.",
                    pass.name, percentage
                ),
            });
        }

        // Long pass detection.
        if percentage > 25.0 {
            bottlenecks.push(Bottleneck {
                name: pass.name.clone(),
                duration_us: pass.gpu_time_us,
                percentage,
                bottleneck_type: BottleneckType::LongPass,
                suggestion: format!(
                    "Pass '{}' takes {:.1}% of frame ({:.2}ms). Consider splitting or optimising.",
                    pass.name, percentage, pass.gpu_time_us / 1000.0
                ),
            });
        }
    }

    bottlenecks.sort_by(|a, b| b.duration_us.partial_cmp(&a.duration_us).unwrap_or(std::cmp::Ordering::Equal));
    bottlenecks
}

