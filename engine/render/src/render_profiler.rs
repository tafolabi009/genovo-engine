// Running Average
// ---------------------------------------------------------------------------

/// Windowed running average for a sampled metric.
#[derive(Debug, Clone)]
pub struct RunningAverage {
    /// Sample buffer.
    pub samples: Vec<f64>,
    /// Current write position.
    pub write_pos: usize,
    /// Number of samples collected.
    pub count: usize,
    /// Window size.
    pub window_size: usize,
}

impl RunningAverage {
    /// Create a new running average with the given window size.
    pub fn new(window_size: usize) -> Self {
        Self {
            samples: vec![0.0; window_size],
            write_pos: 0,
            count: 0,
            window_size,
        }
    }

    /// Add a sample.
    pub fn add_sample(&mut self, value: f64) {
        self.samples[self.write_pos] = value;
        self.write_pos = (self.write_pos + 1) % self.window_size;
        if self.count < self.window_size {
            self.count += 1;
        }
    }

    /// Compute the average of all samples.
    pub fn average(&self) -> f64 {
        if self.count == 0 {
            return 0.0;
        }
        let sum: f64 = self.samples[..self.count].iter().sum();
        sum / self.count as f64
    }

    /// Get the maximum sample value.
    pub fn max(&self) -> f64 {
        if self.count == 0 {
            return 0.0;
        }
        self.samples[..self.count]
            .iter()
            .copied()
            .fold(f64::MIN, f64::max)
    }

    /// Get the minimum sample value.
    pub fn min(&self) -> f64 {
        if self.count == 0 {
            return 0.0;
        }
        self.samples[..self.count]
            .iter()
            .copied()
            .fold(f64::MAX, f64::min)
    }

    /// Get the standard deviation.
    pub fn std_dev(&self) -> f64 {
        if self.count < 2 {
            return 0.0;
        }
        let avg = self.average();
        let variance: f64 = self.samples[..self.count]
            .iter()
            .map(|&v| (v - avg) * (v - avg))
            .sum::<f64>()
            / (self.count - 1) as f64;
        variance.sqrt()
    }

    /// Get the last N samples.
    pub fn last_n(&self, n: usize) -> Vec<f64> {
        let n = n.min(self.count);
        let mut result = Vec::with_capacity(n);
        for i in 0..n {
            let idx = (self.write_pos + self.window_size - n + i) % self.window_size;
            result.push(self.samples[idx]);
        }
        result
    }
}
