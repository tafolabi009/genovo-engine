/// Main render profiler that collects and analyses GPU timing data.
#[derive(Debug)]
pub struct RenderProfiler {
    /// Whether profiling is enabled.
    pub enabled: bool,
    /// Current frame being profiled.
    pub current_frame: RenderFrameProfile,
    /// History of recent frame profiles.
    pub history: Vec<RenderFrameProfile>,
    /// Maximum history length.
    pub max_history: usize,
    /// GPU timestamp frequency (ticks per second).
    pub gpu_frequency: f64,
    /// Current scope stack for hierarchical timing.
    scope_stack: Vec<usize>,
    /// Frame number counter.
    frame_counter: u64,
    /// Per-pass running averages.
    pub pass_averages: HashMap<String, RunningAverage>,
    /// Spike detection threshold factor.
    pub spike_threshold: f32,
    /// Whether to detect bottlenecks.
    pub detect_bottlenecks: bool,
}

impl RenderProfiler {
    /// Create a new render profiler.
    pub fn new() -> Self {
        Self {
            enabled: false,
            current_frame: RenderFrameProfile::new(0),
            history: Vec::new(),
            max_history: AVERAGING_WINDOW,
            gpu_frequency: 1_000_000_000.0,
            scope_stack: Vec::new(),
            frame_counter: 0,
            pass_averages: HashMap::new(),
            spike_threshold: SPIKE_THRESHOLD_FACTOR,
            detect_bottlenecks: true,
        }
    }

    /// Enable or disable profiling.
    pub fn set_enabled(&mut self, enabled: bool) {
        self.enabled = enabled;
    }

    /// Set the GPU timestamp frequency.
    pub fn set_gpu_frequency(&mut self, frequency: f64) {
        self.gpu_frequency = frequency;
    }

    /// Begin a new frame profile.
    pub fn begin_frame(&mut self) {
        if !self.enabled {
            return;
        }
        self.frame_counter += 1;
        self.current_frame = RenderFrameProfile::new(self.frame_counter);
        self.scope_stack.clear();
    }

    /// Begin a timing scope.
    pub fn begin_scope(&mut self, name: &str) -> usize {
        if !self.enabled {
            return 0;
        }

        let parent = self.scope_stack.last().map(|&i| i as i32).unwrap_or(-1);
        let depth = self.scope_stack.len() as u32;
        let index = self.current_frame.timestamps.len();

        self.current_frame.timestamps.push(GpuTimestamp::new(name, depth, parent));
        self.scope_stack.push(index);

        index
    }

    /// End the current timing scope.
    pub fn end_scope(&mut self) {
        if !self.enabled {
            return;
        }
        self.scope_stack.pop();
    }

    /// Add a pass profile to the current frame.
    pub fn add_pass(&mut self, profile: PassProfile) {
        if !self.enabled {
            return;
        }
        self.current_frame.passes.push(profile);
    }

    /// End the current frame and finalize statistics.
    pub fn end_frame(&mut self) {
        if !self.enabled {
            return;
        }

        // Resolve timestamps.
        for timestamp in &mut self.current_frame.timestamps {
            timestamp.resolve(self.gpu_frequency);
        }

        self.current_frame.finalize();

        // Update running averages.
        for pass in &self.current_frame.passes {
            let avg = self.pass_averages
                .entry(pass.name.clone())
                .or_insert_with(|| RunningAverage::new(self.max_history));
            avg.add_sample(pass.gpu_time_us);
        }

        // Store in history.
        let frame = self.current_frame.clone();
        self.history.push(frame);
        if self.history.len() > self.max_history {
            self.history.remove(0);
        }
    }

    /// Get the average GPU time for a specific pass.
    pub fn pass_average(&self, name: &str) -> f64 {
        self.pass_averages.get(name).map(|a| a.average()).unwrap_or(0.0)
    }

    /// Get the maximum GPU time for a specific pass in recent history.
    pub fn pass_max(&self, name: &str) -> f64 {
        self.pass_averages.get(name).map(|a| a.max()).unwrap_or(0.0)
    }

    /// Detect timing spikes for a pass.
    pub fn is_spike(&self, name: &str, current_value: f64) -> bool {
        if let Some(avg) = self.pass_averages.get(name) {
            let threshold = avg.average() * self.spike_threshold as f64;
            current_value > threshold && threshold > 0.0
        } else {
            false
        }
    }

    /// Get the overall frame time average in milliseconds.
    pub fn average_frame_time_ms(&self) -> f64 {
        if self.history.is_empty() {
            return 0.0;
        }
        let sum: f64 = self.history.iter().map(|f| f.total_gpu_us).sum();
        sum / self.history.len() as f64 / 1000.0
    }

    /// Get the 99th percentile frame time in milliseconds.
    pub fn percentile_99_frame_time_ms(&self) -> f64 {
        if self.history.is_empty() {
            return 0.0;
        }
        let mut times: Vec<f64> = self.history.iter().map(|f| f.total_gpu_us).collect();
        times.sort_by(|a, b| a.partial_cmp(b).unwrap_or(std::cmp::Ordering::Equal));
        let idx = (times.len() as f64 * 0.99).ceil() as usize - 1;
        times[idx.min(times.len() - 1)] / 1000.0
    }

    /// Generate a timeline for Chrome Trace format (JSON).
    pub fn export_chrome_trace(&self) -> String {
        let mut events = Vec::new();

        for frame in &self.history {
            for timestamp in &frame.timestamps {
                events.push(format!(
                    r#"{{"name":"{}","ph":"X","ts":{:.0},"dur":{:.0},"pid":1,"tid":{}}}"#,
                    timestamp.name,
                    timestamp.start_ticks as f64 / self.gpu_frequency * 1_000_000.0,
                    timestamp.duration_us,
                    timestamp.depth
                ));
            }
        }

        format!("[{}]", events.join(","))
    }

    /// Get a summary string of the current frame performance.
    pub fn summary(&self) -> String {
        let frame = &self.current_frame;
        let mut summary = format!(
            "Frame {}: GPU {:.2}ms, CPU {:.2}ms, {} draws, {} dispatches\n",
            frame.frame_number,
            frame.total_gpu_ms(),
            frame.total_cpu_ms(),
            frame.total_draw_calls,
            frame.total_dispatches,
        );

        for pass in &frame.passes {
            summary.push_str(&format!(
                "  {}: {:.2}ms ({} draws)\n",
                pass.name,
                pass.gpu_time_ms(),
                pass.draw_calls,
            ));
        }

        if !frame.bottlenecks.is_empty() {
            summary.push_str("Bottlenecks:\n");
            for bottleneck in &frame.bottlenecks {
                summary.push_str(&format!("  {}\n", bottleneck.suggestion));
            }
        }

        summary
    }
}

