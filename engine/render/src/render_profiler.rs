// engine/render/src/render_profiler.rs
//
// GPU render profiler for the Genovo engine.
//
// Provides detailed GPU performance analysis through timestamp queries,
// pipeline statistics, and frame graph timeline construction.
//
// Features:
// - Per-pass timestamp queries for GPU timing.
// - Pipeline statistics queries (vertex/fragment invocations, primitives).
// - Frame graph timeline visualization data.
// - Bottleneck detection based on pass durations and resource usage.
// - Multi-frame averaging and spike detection.
// - Hierarchical scope tree for nested GPU operations.
// - Export to Chrome Trace format for external analysis.

use std::collections::HashMap;

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

/// Maximum number of timestamp query pairs per frame.
const MAX_TIMESTAMP_QUERIES: usize = 256;

/// Number of frames to average for statistics.
const AVERAGING_WINDOW: usize = 60;

/// Threshold factor for spike detection (N times average).
const SPIKE_THRESHOLD_FACTOR: f32 = 2.0;

/// Minimum duration to be considered a bottleneck (in microseconds).
const BOTTLENECK_THRESHOLD_US: f64 = 500.0;

// ---------------------------------------------------------------------------
// GPU Timestamp
// ---------------------------------------------------------------------------

/// A pair of GPU timestamps marking the start and end of a scope.
#[derive(Debug, Clone)]
pub struct GpuTimestamp {
    /// Scope name.
    pub name: String,
    /// Start timestamp (in GPU ticks).
    pub start_ticks: u64,
    /// End timestamp (in GPU ticks).
    pub end_ticks: u64,
    /// Duration in microseconds (computed after resolving timestamps).
    pub duration_us: f64,
    /// Parent scope index (-1 for root).
    pub parent_index: i32,
    /// Depth in the scope hierarchy.
    pub depth: u32,
    /// Color for visualization (RGBA).
    pub color: [f32; 4],
}

impl GpuTimestamp {
    /// Create a new unresolved timestamp scope.
    pub fn new(name: &str, depth: u32, parent: i32) -> Self {
        Self {
            name: name.to_string(),
            start_ticks: 0,
            end_ticks: 0,
            duration_us: 0.0,
            parent_index: parent,
            depth,
            color: Self::color_for_name(name),
        }
    }

    /// Resolve the timestamp ticks to microseconds given the GPU frequency.
    pub fn resolve(&mut self, gpu_frequency: f64) {
        if gpu_frequency > 0.0 && self.end_ticks >= self.start_ticks {
            let ticks = (self.end_ticks - self.start_ticks) as f64;
            self.duration_us = ticks / gpu_frequency * 1_000_000.0;
        }
    }

    /// Generate a deterministic color from the scope name.
    fn color_for_name(name: &str) -> [f32; 4] {
        let mut hash: u32 = 5381;
        for b in name.bytes() {
            hash = hash.wrapping_mul(33).wrapping_add(b as u32);
        }

        let r = ((hash >> 16) & 0xFF) as f32 / 255.0 * 0.5 + 0.3;
        let g = ((hash >> 8) & 0xFF) as f32 / 255.0 * 0.5 + 0.3;
        let b = (hash & 0xFF) as f32 / 255.0 * 0.5 + 0.3;
        [r, g, b, 1.0]
    }
}

// ---------------------------------------------------------------------------
// Pipeline Statistics
// ---------------------------------------------------------------------------

/// GPU pipeline statistics from a query.
#[derive(Debug, Clone, Default)]
pub struct PipelineStats {
    /// Number of vertices submitted to the input assembler.
    pub vertices_submitted: u64,
    /// Number of primitives submitted.
    pub primitives_submitted: u64,
    /// Number of vertex shader invocations.
    pub vertex_shader_invocations: u64,
    /// Number of geometry shader invocations.
    pub geometry_shader_invocations: u64,
    /// Number of primitives generated by the geometry shader.
    pub geometry_shader_primitives: u64,
    /// Number of fragment shader invocations.
    pub fragment_shader_invocations: u64,
    /// Number of compute shader invocations.
    pub compute_shader_invocations: u64,
    /// Number of primitives that passed clipping.
    pub clipping_primitives: u64,
    /// Number of tessellation control shader invocations.
    pub tess_control_invocations: u64,
    /// Number of tessellation evaluation shader invocations.
    pub tess_eval_invocations: u64,
}

impl PipelineStats {
    /// Compute the vertex-to-fragment ratio.
    pub fn vertex_fragment_ratio(&self) -> f64 {
        if self.fragment_shader_invocations == 0 {
            return 0.0;
        }
        self.vertex_shader_invocations as f64 / self.fragment_shader_invocations as f64
    }

    /// Estimate overdraw (fragment invocations / pixels rendered).
    pub fn estimated_overdraw(&self, viewport_pixels: u64) -> f64 {
        if viewport_pixels == 0 {
            return 0.0;
        }
        self.fragment_shader_invocations as f64 / viewport_pixels as f64
    }

    /// Total shader invocations across all stages.
    pub fn total_invocations(&self) -> u64 {
        self.vertex_shader_invocations
            + self.geometry_shader_invocations
            + self.fragment_shader_invocations
            + self.compute_shader_invocations
            + self.tess_control_invocations
            + self.tess_eval_invocations
    }
}

