/// Complete profile data for a single rendered frame.
#[derive(Debug, Clone)]
pub struct RenderFrameProfile {
    /// Frame number.
    pub frame_number: u64,
    /// Total GPU time in microseconds.
    pub total_gpu_us: f64,
    /// Total CPU time in microseconds.
    pub total_cpu_us: f64,
    /// Per-pass profiles.
    pub passes: Vec<PassProfile>,
    /// Timestamp scopes (hierarchical).
    pub timestamps: Vec<GpuTimestamp>,
    /// Detected bottlenecks.
    pub bottlenecks: Vec<Bottleneck>,
    /// Aggregate pipeline statistics.
    pub total_stats: PipelineStats,
    /// Total draw calls across all passes.
    pub total_draw_calls: u32,
    /// Total dispatches across all passes.
    pub total_dispatches: u32,
}

impl RenderFrameProfile {
    /// Create a new frame profile.
    pub fn new(frame_number: u64) -> Self {
        Self {
            frame_number,
            total_gpu_us: 0.0,
            total_cpu_us: 0.0,
            passes: Vec::new(),
            timestamps: Vec::new(),
            bottlenecks: Vec::new(),
            total_stats: PipelineStats::default(),
            total_draw_calls: 0,
            total_dispatches: 0,
        }
    }

    /// Compute totals from individual passes.
    pub fn finalize(&mut self) {
        self.total_gpu_us = self.passes.iter().map(|p| p.gpu_time_us).sum();
        self.total_cpu_us = self.passes.iter().map(|p| p.cpu_time_us).sum();
        self.total_draw_calls = self.passes.iter().map(|p| p.draw_calls).sum();
        self.total_dispatches = self.passes.iter().map(|p| p.dispatches).sum();

        for pass in &self.passes {
            self.total_stats.vertices_submitted += pass.pipeline_stats.vertices_submitted;
            self.total_stats.primitives_submitted += pass.pipeline_stats.primitives_submitted;
            self.total_stats.vertex_shader_invocations += pass.pipeline_stats.vertex_shader_invocations;
            self.total_stats.fragment_shader_invocations += pass.pipeline_stats.fragment_shader_invocations;
            self.total_stats.compute_shader_invocations += pass.pipeline_stats.compute_shader_invocations;
        }

        self.bottlenecks = detect_bottlenecks(&self.passes, self.total_gpu_us);
    }

    /// Returns total GPU time in milliseconds.
    pub fn total_gpu_ms(&self) -> f64 {
        self.total_gpu_us / 1000.0
    }

    /// Returns total CPU time in milliseconds.
    pub fn total_cpu_ms(&self) -> f64 {
        self.total_cpu_us / 1000.0
    }

    /// Get time breakdown by pass category.
    pub fn time_by_category(&self) -> HashMap<PassCategory, f64> {
        let mut result = HashMap::new();
        for pass in &self.passes {
            *result.entry(pass.category).or_insert(0.0) += pass.gpu_time_us;
        }
        result
    }
}

