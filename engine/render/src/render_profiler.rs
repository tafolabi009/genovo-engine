/// Profile data for a single render pass.
#[derive(Debug, Clone)]
pub struct PassProfile {
    /// Pass name.
    pub name: String,
    /// GPU duration in microseconds.
    pub gpu_time_us: f64,
    /// CPU time spent recording commands for this pass (microseconds).
    pub cpu_time_us: f64,
    /// Pipeline statistics for this pass.
    pub pipeline_stats: PipelineStats,
    /// Number of draw calls in this pass.
    pub draw_calls: u32,
    /// Number of dispatches (compute) in this pass.
    pub dispatches: u32,
    /// Render target resolution (width, height).
    pub resolution: (u32, u32),
    /// Number of render targets written.
    pub render_target_count: u32,
    /// Whether this is a compute pass.
    pub is_compute: bool,
    /// Memory bandwidth estimate in MB/s.
    pub bandwidth_estimate_mb: f64,
    /// Pass category for grouping.
    pub category: PassCategory,
}

/// Category for render pass grouping.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum PassCategory {
    /// Geometry rendering (G-buffer, forward).
    Geometry,
    /// Shadow map rendering.
    Shadow,
    /// Lighting computation.
    Lighting,
    /// Post-processing effects.
    PostProcess,
    /// UI rendering.
    Ui,
    /// Compute passes.
    Compute,
    /// Debug visualization.
    Debug,
    /// Transfer/copy operations.
    Transfer,
    /// Other / uncategorized.
    Other,
}

impl PassProfile {
    /// Create a new pass profile.
    pub fn new(name: &str, category: PassCategory) -> Self {
        Self {
            name: name.to_string(),
            gpu_time_us: 0.0,
            cpu_time_us: 0.0,
            pipeline_stats: PipelineStats::default(),
            draw_calls: 0,
            dispatches: 0,
            resolution: (0, 0),
            render_target_count: 0,
            is_compute: false,
            bandwidth_estimate_mb: 0.0,
            category,
        }
    }

    /// Returns the pass duration in milliseconds.
    pub fn gpu_time_ms(&self) -> f64 {
        self.gpu_time_us / 1000.0
    }

    /// Returns the CPU time in milliseconds.
    pub fn cpu_time_ms(&self) -> f64 {
        self.cpu_time_us / 1000.0
    }
}

